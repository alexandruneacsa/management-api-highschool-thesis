<h1>Despre noi</h1>
<p>Jiu-Jitsu Brazilian, cunoscut și ca BJJ, este un sport de contact care se bazează pe lupta la sol și care prin folosirea tehnicilor articulare și a strangulărilor îl forțează pe oponent să renunțe la luptă. Pe principiul folosirii cât mai eficiente a tehnicii și în special a pârghiilor, în dauna atributelor fizice (greutate, forță explozivă, alonj iiă etc.), un practicant fără atribute fizice de excepție are șanse de reușită ridicate în luptă, în fața unui adversar superior din punct de vedere fizic. Stilul a fost creat la începutul secolului 20 de către familia Gracie, în Brazilia. BJJ-ul poate fi practicat si pentru auto-apărare alaturi de late tehnici de lupta corp la corp, pentru aspectul competițional (Gi și No Gi) sau pentru competițiile de arte marțiale mixte (Mixed Martial Arts sau MMA).</p>

